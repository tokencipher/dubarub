    <h2 class="title">Crime In The City</h2>
    <img id="post3" src="img/clubBlu1.jpg" alt="clubBlu" height="385" style="width:100%">
    <div class="metadata">
      <span class="credit">(Photo: Bryan Thomas)</span>
      <br>
      <br>
      <p style="margin-left:10px">
        <a href="#"><i class="fa fa-hashtag fa-lg" aria-hidden="true"></i>Politics</a>
        <i class="fa fa-plug fa-lg" aria-hidden="true" style="margin-left:5px;padding-right:2px;"></i>Bryan Thomas</a>
        <i class="fa fa-calendar-o fa-lg" aria-hidden="true" style="margin-left:5px;padding-right:2px;"></i>07/26/2016
      </p>
      <hr>
    </div>
    <p class="entry"><span id="location">(Fort Myers, FL) - </span>It was a Sunday night. I had just gotten
      back from spending time with my family in Port Charlotte. Not really tired, I decided to work on my
      website -- figuring out how I should go about streaming music from my server. I'd say it was around
      12:34pm when I heard a session of gun shots (about 30) being fired. I couldn't believe my ears. I 
      was in utter disbelief. I knew my neighborhood wasn't the greatest, but not this bad. After gathering
      myself, before you know it, I was out the front door and off in my car to check out what's going on.
      There's a firestation off of the street I live on and as I was passing by the garage was opening. 
    </p>
    <p class="entry">I got to the stop sign and made a left onto 41; the firetruck did as well then turned
      his sirens on. After moving to the side, that's when I seen two cars frantically turning off of the
      street the firetruck had turned onto. I continued to follow the firetruck where I was then led to the
      scene. Once I pulled up and got out of my car, all I could hear was a woman screaming in a crowd 
      full of police and onlookers. I did what I could to ask questions to find out what happened. 
    </p>
    <p class="entry">I walked to the front door of the club where there was a group of girls and a female 
      police officer motioning everyone to either go inside the club or leave. I didn't leave. Behind
      the police officer was a young black guy on the floor lying dead on his back in the parking lot. 
      There was a friend of the victim right beside me calling out his friends name in anguish and 
      disbelief. I couldn't believe it myself. This stuff happening right in my backyard. I asked another
      kid what happened and I remember him telling me -- "There was a performance inside the club. While
      the performance was happening there was guys in there throwing up flags and yelling out sets. This 
      is nothing, but some hood beef." 
    </p>
    <p class="entry">'Hood beef' that costed the lives of 2 and injured 14? Ignorance; it's bliss, isn't it?
      All Lives Matter. Let me repeat: All Lives Matter. To these guys, evidently, it was the opposite.
      I'm absolutely disgusted at this point. I'm pissed off. Not even the kids are safe! This club was 
      supposed to be where they go to have a good time and look. Ruined. What really irks me to no end --
      is that if you were to try to get some type of rationale out of the heinous act of violence that was
      committed there would be absolutely no justification. None. It's senseless!      
    <p class="entry">My condolences and prayers goes out to the families of the victims and injured. I
      also pray for the world to become a better, safer place. You all should too. God Bless.
    </p> 
